<h3>What is Cloud Computing?</h3>
    <p>Cloud computing in the raw form simply means that you are accessing data remotely over the internet, this could be you checking you GMail account, your HDSB Email, your marks for this course. It all can be qualified
    as cloud computing to an extent. When people think of cloud computing, they think of either "invisible" computers that will do everything for them, or they will think of a giant datacentre which
    which holds the world's data, ready for all people to access it.</p>
    <p>The cloud computing market is absoloutely massive, to give it some scale, <a href="http://247wallst.com/technology-3/2015/03/07/the-270-billon-cloud-computing-market/">it is thought that it will be
        worth $270 Billion per year by 2020.</a></p>
    <p>Now, if you have a NAS(Network Attached Storage) server at home and you are accessing the backups or whatever files on it from the same network, that's not cloud computing. However, if you were to
    expose that server to the internet for you to access outside the network, that would be cloud computing in the sense of data storage. Which gets us to the next point, the different types of cloud.</p>
    <p>Just for some showey offey time, here is my personal cloud/home server setup. I use it to remotely run my entire house, meaning all my information is stored on these servers. I am planning
    on upgrading shortly, due to the servers being archaic nearly. This is a cloud because it runs remote access clients, such as my VPN server, as well as a slew of VM's on VMWare VCenter.</p>
