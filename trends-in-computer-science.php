<h3>Types of Cloud</h3>
<p>These are "explanations" of the IaaS/SaaS/PaaS Pyramid</p>
<ul>
    <li><b>Storage Cloud</b> - Such as Microsoft OneDrive or Google Drive</li>
    <li><b>Backup Cloud</b> - Such as Backblaze, or another offsite backup platform</li>
    <li><b>Application Cloud</b> - Such as Microsoft Office 365 or Office Online where the application is hosted remotely for you to access at any time</li>
    <li><b>Private Cloud</b> - Such as a remotely hosted VPS or Dedicated server for a company to use for any of the above or below uses.</li>
    <li><b>Public Cloud</b> - Shared hosting, such as web hosting.</li>
    <li><b>Compute Cloud</b> - Remote computing cloud, such as Nvidia Grid - Which is a remotely hosted extremely powerful computing server.</li>
</ul>
